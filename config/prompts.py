"""
各平台AI Prompt模板
"""

X_PROMPT = """你将一篇技术博客转化为Twitter线程。

原文信息：
- 标题：{title}
- 摘要：{summary}
- 核心观点：{key_points}
- 代码片段：{code_snippets}

要求：
1. 生成5-8条推文组成的线程
2. 第1条必须是hook，吸引技术读者点击阅读原文
3. 每条≤280字符（英文）或≤140字符（中文）
4. 代码片段单独成帖，注明编程语言
5. 技术概念必须准确，不简化到错误
6. 结尾帖引导互动（提问或邀请讨论）
7. 添加2-3个相关技术hashtag
8. 如果是中文内容，使用中文撰写；如果是英文内容，使用英文撰写

输出格式：JSON数组，每个元素是一条推文字符串。不要包含任何其他解释文字。
示例输出：
["第一条推文内容...", "第二条推文内容...", "第三条推文内容..."]
"""

ZHIHU_PROMPT = """你将一篇技术博客转化为知乎回答/文章风格。

原文信息：
- 标题：{title}
- 正文：{content}
- 核心观点：{key_points}
- 代码片段：{code_snippets}

要求：
1. 使用知乎体：专业但易懂，开头自然引入话题（不需要"谢邀"）
2. 结构清晰：引言→问题背景→核心方案→代码示例→注意事项→总结
3. 代码块保持完整，使用Markdown代码块格式，标注语言
4. 关键概念加粗（**加粗文本**）
5. 在适当位置插入个人经验或踩坑记录增强真实感
6. 结尾有互动引导（"你遇到过类似问题吗？"或"欢迎在评论区交流"）
7. 如果是中文内容，使用中文撰写；如果是英文内容，使用英文撰写

输出格式：完整Markdown文本。不要包含任何其他解释文字。
"""

XIAOHONGSHU_PROMPT = """你将一篇技术博客转化为小红书风格的技术分享笔记。

原文信息：
- 标题：{title}
- 核心观点：{key_points}
- 关键数据/效果：{metrics}

要求：
1. 标题 catchy，带emoji，20字以内
   示例："🔥3行Python代码搞定XX，效率提升10倍"
2. 正文：
   - 每段3-5行，段间空行
   - 每段开头或结尾加emoji
   - 语气像跟朋友分享技术发现，极度口语化
   - 保留核心技术点但不用复杂术语
   - ≤800字（小红书最佳阅读长度）
3. 标签：5-8个，格式如 #程序员 #Python #效率工具 #技术分享
4. 配图提示词：1-2句描述配图内容（供AI绘图工具使用）
5. 结尾引导："收藏备用"或"评论区交流"或"关注我，持续分享技术干货"
6. 如果是中文内容，使用中文撰写；如果是英文内容，先翻译为中文再改写为小红书风格

输出格式：JSON对象，包含以下字段：
- title: 标题字符串
- content: 正文字符串
- tags: 标签数组
- image_prompt: 配图提示词字符串

不要包含任何其他解释文字。
"""

STYLE_LEARNING_PROMPT = """分析以下参考文本的写作风格，提取风格特征用于后续内容生成。

参考文本：
{reference_texts}

请提取以下风格特征（JSON格式）：
- average_sentence_length: 平均句长（短/中/长）
- emoji_usage: emoji使用频率（高/中/低/无）
- tone: 语气（正式/随意/幽默/专业/亲和）
- vocabulary_level: 词汇复杂度（简单/中等/复杂）
- favorite_phrases: 常用口头禅或过渡语（数组）
- punctuation_style: 标点风格（如喜欢用"~"、"..."、"！"等）
- paragraph_structure: 段落结构偏好（短段落/长段落/列表式）

输出格式：JSON对象。不要包含任何其他解释文字。
"""

QUALITY_SCORE_PROMPT = """对以下社交媒体内容进行质量评分。

原文核心信息：
{original_key_points}

生成的内容（平台：{platform}）：
{generated_content}

请从以下9个维度评分（每项0-10分，保留1位小数），并给出总分和改进建议：

1. 信息完整性(15%): 是否遗漏原文核心技术点
2. 平台适配度(15%): 是否符合目标平台的内容消费习惯
3. 语言流畅度(15%): 是否自然，无AI生成痕迹
4. 技术准确性(15%): 代码、术语、概念是否正确
5. 吸引力(10%): 首句/标题是否有hook
6. 可读性(10%): 段落长度、标点、格式是否友好
7. 风格一致性(10%): 是否与参考风格一致
8. CTA完整性(5%): 是否有合适的行动引导
9. 原创性(5%): 是否避免与原文过度重复

输出格式：JSON对象，包含 scores(各维度分数)、total(总分)、suggestions(改进建议数组)。
不要包含任何其他解释文字。
"""


def get_prompt(platform: str) -> str:
    """获取指定平台的Prompt模板"""
    prompts = {
        "x": X_PROMPT,
        "zhihu": ZHIHU_PROMPT,
        "xiaohongshu": XIAOHONGSHU_PROMPT,
    }
    if platform not in prompts:
        raise ValueError(f"不支持的平台: {platform}")
    return prompts[platform]
